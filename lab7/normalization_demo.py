"""
Занятие 7. Демонстрация процесса нормализации.
Показывает переход от ненормализованной таблицы к 3НФ.
"""
 
 
def show_unnormalized():
    """Ненормализованная таблица."""
    print("=" * 65)
    print("НЕНОРМАЛИЗОВАННАЯ ТАБЛИЦА (заказы интернет-магазина)")
    print("=" * 65)
 
    data = [
        ("001", "Иванов", "Москва, ул. Ленина 5",
         "Клавиатура, Мышь", "1500, 800"),
        ("002", "Петров", "СПб, Невский 10",
         "Монитор", "25000"),
        ("003", "Иванов", "Москва, ул. Ленина 5",
         "Наушники, Веб-камера, Коврик", "3000, 5000, 500"),
    ]
 
    header = f"{'ID':<5} {'Клиент':<10} {'Адрес':<25} {'Товары':<25} {'Цены'}"
    print(header)
    print("-" * 65)
    for row in data:
        print(f"{row[0]:<5} {row[1]:<10} {row[2]:<25} {row[3]:<25} {row[4]}")
 
    print()
    print("ПРОБЛЕМЫ:")
    print("  1. Неатомарные значения (товары и цены через запятую)")
    print("  2. Дублирование данных клиента (Иванов повторяется)")
    print("  3. Транзитивная зависимость: адрес зависит от клиента")
 
 
def show_1nf():
    """Первая нормальная форма."""
    print()
    print("=" * 60)
    print("1НФ: Атомарные значения (каждый товар --- отдельная строка)")
    print("=" * 60)

    data = [
        ("001", "Иванов", "Москва, ул. Ленина 5", "Клавиатура", 1500),
        ("001", "Иванов", "Москва, ул. Ленина 5", "Мышь", 800),
        ("002", "Петров", "СПб, Невский 10", "Монитор", 25000),
        ("003", "Иванов", "Москва, ул. Ленина 5", "Наушники", 3000),
        ("003", "Иванов", "Москва, ул. Ленина 5", "Веб-камера", 5000),
        ("003", "Иванов", "Москва, ул. Ленина 5", "Коврик", 500),
    ]
 
    header = f"{'ID':<5} {'Клиент':<10} {'Адрес':<25} {'Товар':<15} {'Цена'}"
    print(header)
    print("-" * 60)
    for row in data:
        print(f"{row[0]:<5} {row[1]:<10} {row[2]:<25} {row[3]:<15} {row[4]}")
 
    print("\nПроблема: дублирование клиента и адреса!")
 
 
def show_3nf():
    """Третья нормальная форма (разделение на таблицы)."""
    print()
    print("=" * 50)
    print("3НФ: Три таблицы без избыточности")
    print("=" * 50)
 
    print("\nТаблица КЛИЕНТЫ (clients):")
    print(f"  {'id':<5} {'name':<10} {'address'}")
    print(f"  {'1':<5} {'Иванов':<10} {'Москва, ул. Ленина 5'}")
    print(f"  {'2':<5} {'Петров':<10} {'СПб, Невский 10'}")
 
    print("\nТаблица ЗАКАЗЫ (orders):")
    print(f"  {'id':<5} {'client_id':<12} {'date'}")
    print(f"  {'1':<5} {'1':<12} {'2025-01-15'}")
    print(f"  {'2':<5} {'2':<12} {'2025-01-16'}")
    print(f"  {'3':<5} {'1':<12} {'2025-01-17'}")
 
    print("\nТаблица ПОЗИЦИИ ЗАКАЗА (order_items):")
    print(f"  {'id':<5} {'order_id':<10} {'product':<15} {'price'}")
    print(f"  {'1':<5} {'1':<10} {'Клавиатура':<15} {'1500'}")
    print(f"  {'2':<5} {'1':<10} {'Мышь':<15} {'800'}")
    print(f"  {'3':<5} {'2':<10} {'Монитор':<15} {'25000'}")
    print(f"  {'4':<5} {'3':<10} {'Наушники':<15} {'3000'}")
 
    print("\nИтог: нет дублирования, нет аномалий!")
 
 
if __name__ == "__main__":
    show_unnormalized()
    show_1nf()
    show_3nf()

